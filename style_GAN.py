def create_model(opts):
    """Builds the generators and discriminators.
    """
    model_dict = {'cycle': CycleGenerator}
    G = model_dict[opts.gen](conv_dim=opts.g_conv_dim, norm=opts.norm) 

    model_dict = {'dc': DCDiscriminator, 'patch': PatchDiscriminator}
    D = model_dict[opts.disc](conv_dim=opts.d_conv_dim, norm=opts.norm) 

    style_iden = model_dict[opts.style_iden](conv_dim=opts.d_conv_dim, norm=opts.norm) 

    
    print_models(G, D, style_iden)

    if torch.cuda.is_available():
        G.cuda()
        D.cuda()
        style_iden.cuda() 
        print('Models moved to GPU.')

    return G, D, style_iden


def checkpoint(iteration, G, D, g_optimizer, d_optimizer, opts):
    """Save generators G_YtoX, G_XtoY and discriminators D_X, D_Y."""
    G_path = os.path.join(
        opts.checkpoint_dir, '%ditr/G.pkl' % iteration
    )
    D_path = os.path.join(
        opts.checkpoint_dir, '%ditr/D.pkl' % iteration
    ) 
    g_opt_path = os.path.join(opts.checkpoint_dir, '%ditr/g_optimizer.pkl' % iteration)
    d_opt_path = os.path.join(opts.checkpoint_dir, '%ditr/d_optimizer.pkl' % iteration)
    
    torch.save(G.state_dict(), G_path)
    torch.save(D.state_dict(), D_path) 
    torch.save(g_optimizer.state_dict(), g_opt_path)
    torch.save(d_optimizer.state_dict(), d_opt_path)

